var MathUtil = /** @class */ (function () {
    /**
     *
     */
    function MathUtil() {
    }
    /**
     * 加法
     * @param x 被加数
     * @param y 加数
     */
    MathUtil.add = function (x, y) {
        return x + y;
    };
    /**
     * 减法
     * @param x 被减数
     * @param y 减数
     */
    MathUtil.minus = function (x, y) {
        return x - y;
    };
    return MathUtil;
}());
