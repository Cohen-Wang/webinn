

class MathUtil {

    name : 'cohen';
    /**
     *
     */
    constructor () {

    }

    /**
     * 加法
     * @param x 被加数
     * @param y 加数
     */
    static add (x: number, y: number): number {
        return x + y;
    }

    /**
     * 减法
     * @param x 被减数
     * @param y 减数
     */
    static minus (x: number, y: number): number {
        return x - y;
    }



}