(function () {

    /**
     * 炮台
     */
    let Battery = window.Battery = function () {
        // 炮塔的图片
        this.image = fishne.R.battery;
        // 变量
        this.w = this.image.naturalWidth;
        this.h = this.image.naturalHeight;
        this.x = fishne.can.width / 2 - this.w / 2;
        this.y = fishne.can.height - this.h - 20;// 20 是离底部的距离
        // 弧度
        this.angel = 0;
    };

    /**
     * init
     */
    Battery.prototype.init = function () {

    };

    /**
     * update
     */
    Battery.prototype.update = function () {

    };

    /**
     * render
     */
    Battery.prototype.render = function () {
        fishne.ctx.save();
        fishne.ctx.beginPath();
        fishne.ctx.translate(this.x, this.y);// 设置原点
        fishne.ctx.rotate(this.angel);
        fishne.ctx.drawImage(this.image, 0, 0, this.w, this.h, -this.w/2, -this.h/2, this.w, this.h);// 注意设置原点的地方
        fishne.ctx.closePath();
        fishne.ctx.restore();
    };



}) (window);